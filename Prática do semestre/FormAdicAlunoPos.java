    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        AlunoPos ap;
        String dn = jFormattedTextField2.getText();
        int dia = Integer.parseInt(dn.substring(0,2));
        int mes = Integer.parseInt(dn.substring(3,5));
        int ano = Integer.parseInt(dn.substring(6,10));
        Calendar data = Calendar.getInstance();
        data.set(ano, mes - 1,dia);
        ap = new AlunoPos(Integer.parseInt(jFormattedTextField1.getText()),jTextField1.getText(),data,(String)jComboBox1.getSelectedItem(),jTextField2.getText());
        if(AppMenu.AddAluno(ap))
             JOptionPane.showMessageDialog(FormAdicAlunoPos.this, ap.toString(),"Aluno Cadastrado com Sucesso", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(FormAdicAlunoPos.this, "Aluno já está cadastrado ou Armazenamento cheio!", "Erro", JOptionPane.ERROR_MESSAGE);
        
    }//GEN-LAST:event_jButton1ActionPerformed

public static DefaultFormatterFactory setFormatoMatricula()  
{  
    MaskFormatter comFoco = null;  
    try   
    {   
        comFoco = new MaskFormatter("#######");  
    }   
    catch (Exception pe) { }  
    DefaultFormatterFactory factory = new DefaultFormatterFactory(comFoco, comFoco);  
    return factory;  
}

public static DefaultFormatterFactory setFormatoData()  
{  
    MaskFormatter comFoco = null;  
    try   
    {   
        comFoco = new MaskFormatter("##/##/####");  
    }   
    catch (Exception pe) { }  
    DefaultFormatterFactory factory = new DefaultFormatterFactory(comFoco, comFoco);  
    return factory;  
}

