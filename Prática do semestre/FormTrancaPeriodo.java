    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       boolean achou = false;
       pos = 0;
        if(!jFormattedTextField1.getText().equals("")){
            for(Aluno a: cad){
                if(Integer.parseInt(jFormattedTextField1.getText()) == a.getMatricula() ){
                    jTextField1.setText(a.getNome());
                    achou = true;
                    break;
                }
                pos++;
            }
            if (achou){
                if (cad.get(pos) instanceof AlunoPos){
                    JOptionPane.showMessageDialog(FormTrancaPeriodo.this, "Aluno de Pós Graduação não pode trancar o período", "Erro", JOptionPane.ERROR_MESSAGE);
                    jButton2.setEnabled(false);
                }  
            }
            if (!achou) JOptionPane.showMessageDialog(FormTrancaPeriodo.this, "Aluno Inexistente!!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(FormTrancaPeriodo.this, "Preencha o Campo Matrícula!!", "Erro", JOptionPane.ERROR_MESSAGE); 
    }//GEN-LAST:event_jButton1ActionPerformed

